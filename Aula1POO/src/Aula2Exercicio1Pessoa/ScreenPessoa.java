    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       pessoa = new Pessoa(txtNome.getText(), txtCpf.getText(), 
                (txtSexo.getText().charAt(0)), Integer.parseInt(txtIdade.getText()));
        
        JOptionPane.showMessageDialog(rootPane, txtNome.getText()+ " seu cadastro foi efetuado com sucesso");
        
        // resetar Texts Fields
        txtNome.setText("");
        txtCpf.setText("");
        txtSexo.setText("");
        txtIdade.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarActionPerformed
         if (pessoa == null) {
            return;
        }
        
        JOptionPane.showMessageDialog(rootPane, pessoa.imprimirPessoa());
    }//GEN-LAST:event_btnVisualizarActionPerformed

