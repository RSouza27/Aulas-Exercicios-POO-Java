    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int cracha = Integer.parseInt(txtCracha.getText());
        char tipoVinculo = cbTipoVinculo.getSelectedItem().toString().charAt(0);
        float salario = Float.parseFloat(txtSalario.getText());
        float valorDesconto = Float.parseFloat(txtValorDesconto.getText());
        float valorHora = Float.parseFloat(txtValorHora.getText());
        float qtdeHora = Float.parseFloat(txtQtdeHora.getText()); 
            
        if (tipoVinculo == 'N') {
           funcionario = new Funcionario(cracha, txtNome.getText(), tipoVinculo, salario, valorDesconto);
           //funcionario = new Funcionario(cracha, txtNome.getText(), tipoVinculo, 0, 0, valorDesconto); 
        } else {
            funcionario = new Funcionario(cracha, txtNome.getText(), tipoVinculo, valorHora, qtdeHora, valorDesconto);
        }
        
        JOptionPane.showMessageDialog(rootPane, funcionario.getNome()+" cadastrado com sucesso!");
    }//GEN-LAST:event_btnCadastrarActionPerformed

