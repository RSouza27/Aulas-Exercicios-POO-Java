    private void cbTipoVinculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipoVinculoActionPerformed
        if (cbTipoVinculo.getSelectedIndex() == 0) {
            txtValorHora.setEnabled(false);
            txtQtdeHora.setEnabled(false);
            txtSalario.setEnabled(true);
        } else {
            txtSalario.setEnabled(false);
            txtValorHora.setEnabled(true);
            txtQtdeHora.setEnabled(true);
        }
    }//GEN-LAST:event_cbTipoVinculoActionPerformed

